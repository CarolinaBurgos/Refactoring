public class Animal {

    private int numberOfLegs;
    private String name;
    private String breed;

    public int getNumberOfLegs() {
        return numberOfLegs;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getBreed() {
        return breed;
    }

    public void setBreed(String breed) {
        this.breed = breed;
    }
    
    
    
}
